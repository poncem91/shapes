package Project02;

/**
 * Interface for a Regular Polygon that calculates its area and perimeter.
 *
 * @author Mariafe Ponce
 */

public interface RegularPolygon {

    /**
     * Calculates the regular polygon's area
     *
     * @return calculated area
     */
    double area();

    /**
     * Calculates the regular polygon's perimeter
     *
     * @return calculated perimeter
     */
    double perimeter();
}
